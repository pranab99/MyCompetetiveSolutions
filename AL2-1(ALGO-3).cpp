bool primeALGO3(int n)
{

    if (n <= 1)
        return false;
    if (n <= 3)
        return true;
  
    if (n % 2 == 0 || n % 3 == 0)
        return false;
  
    for (int i = 5; i * i <= n; i = i + 6)
        if (n % i == 0 || n % (i + 2) == 0)
            return false;

        return true;
  
}

void solve() {

	int n;
	cin>>n;

	if(primeALGO3(n))
		cout<<"The no is prime";
	else
		cout<<"The no is not prime";


}





